static inline int __ws_qrbuild(int M, int N, int lb)
{
    return lb > 0 ? lb * N : N;
}

/*
 * Unblocked code for generating M by N matrix Q with orthogonal columns which
 * are defined as the first N columns of the product of K first elementary
 * reflectors.
 *
 * Parameters nk = n(A)-K, mk = m(A)-K define the initial partitioning of
 * matrix A.
 *
 *  Q = H(1)H(2)...H(k)  , 0 < k <= N, where H(i) = I - tau*v*v.T
 *
 * Computation is ordered as H(1)*(H(2)...*(H(k)*I)) ie. from right to left.
 *
 * If k < N columns k+1:N are cleared and diagonal entries [k+1:N,k+1:N] are
 * set to unit. Then the matrix Q is generated by left multiplying elements right
 * of i'th elementary reflector H(i).
 * 
 * Compatible to lapack.xORG2R subroutine.
 */
static
int unblk_qrbuild(armas_x_dense_t * A, armas_x_dense_t * tau,
                  armas_x_dense_t * W, int mk, int nk, int mayclear,
                  armas_conf_t * conf)
{
    armas_x_dense_t ATL, ATR, ABR, A00, a01, a11, a12, a21, A22;
    armas_x_dense_t tT, tB, t0, t1, t2, w12, D;
    DTYPE tauval;
    DTYPE *wdata = armas_x_data(W);

    EMPTY(ATL);
    EMPTY(A00);
    EMPTY(a11);

    mat_partition_2x2(
        &ATL, &ATR,
        __nil, &ABR, /**/ A, mk, nk, ARMAS_PBOTTOMRIGHT);
    mat_partition_2x1(
        &tT,
        &tB, /**/ tau, nk, ARMAS_PBOTTOM);

    // zero the right side
    if (nk > 0 && mayclear) {
        armas_x_mscale(&ATR, ZERO, 0, conf);
        armas_x_mscale(&ABR, ZERO, 0, conf);
        armas_x_diag(&D, &ABR, 0);
        armas_x_madd(&D, ONE, 0, conf);
    }

    while (ATL.rows > 0 && ATL.cols > 0) {
        mat_repartition_2x2to3x3(
            &ATL,
            &A00, &a01, __nil,
            __nil, &a11, &a12,
            __nil, &a21, &A22, /**/ A, 1, ARMAS_PTOPLEFT);
        mat_repartition_2x1to3x1(
            &tT,
            &t0, &t1, &t2, /**/ tau, 1, ARMAS_PTOP);
        // ---------------------------------------------------------------------
        armas_x_make(&w12, a12.cols, 1, a12.cols, wdata);
        armas_x_apply_householder2x1(&t1, &a21,
                                     &a12, &A22, &w12, ARMAS_LEFT, conf);

        tauval = armas_x_get(&t1, 0, 0);
        armas_x_scale(&a21, -tauval, conf);
        armas_x_set(&a11, 0, 0, ONE - tauval);

        // zero
        armas_x_scale(&a01, 0.0, conf);
        // ---------------------------------------------------------------------
        mat_continue_3x3to2x2(
            &ATL, &ATR,
            __nil, &ABR, /**/ &A00, &a11, &A22, A, ARMAS_PTOPLEFT);
        mat_continue_3x1to2x1(
            &tT,
            &tB, /**/ &t0, &t1, tau, ARMAS_PTOP);
    }
    return 0;
}

/*
 * Blocked code for generating M by N matrix Q with orthogonal columns which
 * are defined as the first N columns of the product of K first elementary
 * reflectors.
 *
 * If the number K of elementary reflectors is not multiple of the blocking
 * factor lb, then unblocked code is used first to generate the lower right corner
 * of the matrix Q. 
 *
 * Compatible with lapack.DORGQR subroutine.
 */
static
int blk_qrbuild(armas_x_dense_t * A, armas_x_dense_t * tau,
                armas_x_dense_t * T, armas_x_dense_t * W, int K, int lb,
                armas_conf_t * conf)
{
    armas_x_dense_t ATL, ATR, ABR, A00, A01, A11, A12, A21, A22, AL;
    armas_x_dense_t tT, tB, t0, t1, t2, D, Wrk;
    int nk, mk, uk;

    nk = A->cols - K;
    mk = A->rows - K;
    uk = K % lb;

    EMPTY(ATL);
    EMPTY(A00);

    mat_partition_2x2(
        &ATL, &ATR,
        __nil, &ABR, /**/ A, mk + uk, nk + uk, ARMAS_PBOTTOMRIGHT);
    mat_partition_2x1(
        &tT,
        &tB, /**/ tau, nk + uk, ARMAS_PBOTTOM);

    // zero the right side
    if (nk + uk > 0) {
        armas_x_mscale(&ATR, ZERO, 0, conf);
        if (uk > 0) {
            // blocking factor not multiple of K, do the first uk
            // columns with unblocked code
            unblk_qrbuild(&ABR, &tB, W, ABR.rows-uk, ABR.cols-uk, TRUE, conf);
        } else {
            // blocking factor is multiple of K
            armas_x_mscale(&ABR, ZERO, 0, conf);
            armas_x_diag(&D, &ABR, 0);
            armas_x_madd(&D, ONE, 0, conf);
        }
    }

    while (ATL.rows > 0 && ATL.cols > 0) {
        mat_repartition_2x2to3x3(
            &ATL,
            &A00,  &A01, __nil,
            __nil, &A11, &A12,
            __nil, &A21, &A22, /**/ A, lb, ARMAS_PTOPLEFT);
        mat_repartition_2x1to3x1(
            &tT,
            &t0, &t1, &t2, /**/ tau, lb, ARMAS_PTOP);
        // ---------------------------------------------------------------------
        mat_merge2x1(&AL, &A11, &A21);

        // block reflector
        armas_x_unblk_qr_reflector(T, &AL, &t1, conf);

        // update rightside i.e A12, A22
        armas_x_make(&Wrk, A12.cols, A12.rows, A12.cols, armas_x_data(W));
        armas_x_update_qr_left(&A12, &A22, &A11, &A21, T, &Wrk, FALSE, conf);

        // update current block
        unblk_qrbuild(&AL, &t1, W, A21.rows, 0, FALSE, conf);

        // zero top rows
        armas_x_mscale(&A01, ZERO, 0, conf);
        // ---------------------------------------------------------------------
        mat_continue_3x3to2x2(
            &ATL, &ATR,
            __nil, &ABR, /**/ &A00, &A11, &A22, A, ARMAS_PTOPLEFT);
        mat_continue_3x1to2x1(
            &tT,
            &tB, /**/ &t0, &t1, tau, ARMAS_PTOP);
    }
    return 0;
}


/**
 * \brief Generate the orthogonal matrix Q
 *
 * Generate the M by N matrix Q with orthogonal columns which
 * are defined as the first N columns of the product of K first elementary
 * reflectors.
 *
 * \param[in,out]  A
 *   On entry, the elementary reflectors as returned by qrfactor().
 *   stored below diagonal of the M by N matrix A.
 *   On exit, the orthogonal matrix Q
 * \param[in]  tau
 *   Scalar coefficents of elementary reflectors
 * \param[out]   W
 *    Workspace
 * \param[in]  K
 *    The number of elementary reflectors whose product define the matrix Q
 * \param[in,out] conf
 *    Blocking configuration
 *
 * Compatible with lapackd.ORGQR.
 * \ingroup lapack
 */
int armas_x_qrbuild(armas_x_dense_t * A,
                    const armas_x_dense_t * tau, int K, armas_conf_t * cf)
{
    if (!cf)
        cf = armas_conf_default();

    armas_wbuf_t *wbs, wb = ARMAS_WBNULL;
    if (armas_x_qrbuild_w(A, tau, K, &wb, cf) < 0)
        return -1;

    wbs = &wb;
    if (wb.bytes > 0) {
        if (!armas_walloc(&wb, wb.bytes)) {
            cf->error = ARMAS_EMEMORY;
            return -1;
        }
    } else
        wbs = ARMAS_NOWORK;
    int stat = armas_x_qrbuild_w(A, tau, K, wbs, cf);
    armas_wrelease(&wb);
    return stat;
}


/**
 * @brief Generate the orthogonal matrix Q
 *
 * Generate the M by N matrix Q with orthogonal columns which
 * are defined as the first N columns of the product of K first elementary
 * reflectors.
 *
 * @param[in,out]  A
 *   On entry, the elementary reflectors as returned by armas_x_qrfactor().
 *   stored below diagonal of the M by N matrix A.
 *   On exit, the orthogonal matrix Q
 *
 * @param[in]  tau
 *   Scalar coefficents of elementary reflectors
 *
 * @param[in]  K
 *    The number of elementary reflectors whose product define the matrix Q
 *
 * @param[in]  wb
 *    Workspace buffer needed for computation. To compute size of the required space call 
 *    the function with workspace bytes set to zero. Size of workspace is returned in 
 *    `wb.bytes` and no other computation or parameter size checking is done and function
 *    returns with success.
 *
 * @param[in,out] conf
 *    Configuration options.
 *
 *  @retval 0  success
 *  @retval -1 error and `conf.error` set to last error
 *
 *  Last error codes returned
 *   - `ARMAS_EINVAL` A or tau is null pointer
 *   - `ARMAS_EWORK`  if no workspace or it is less than required for unblocked computation
 *
 * Compatible with lapackd.ORGQR.
 * \ingroup lapack
 */
int armas_x_qrbuild_w(armas_x_dense_t * A,
                      const armas_x_dense_t * tau,
                      int K, armas_wbuf_t * wb, armas_conf_t * conf)
{
    armas_x_dense_t T, Wrk;
    armas_env_t *env;
    size_t wsmin, wsz = 0;
    int lb;
    DTYPE *buf;

    if (!conf)
        conf = armas_conf_default();

    if (!A) {
        conf->error = ARMAS_EINVAL;
        return -1;
    }
    env = armas_getenv();
    if (wb && wb->bytes == 0) {
        if (env->lb > 0 && A->cols > env->lb)
            wb->bytes = (A->cols * env->lb) * sizeof(DTYPE);
        else
            wb->bytes = A->cols * sizeof(DTYPE);
        return 0;
    }

    if (!tau) {
        conf->error = ARMAS_EINVAL;
        return -1;
    }

    lb = env->lb;
    wsmin = A->cols * sizeof(DTYPE);
    if (!wb || (wsz = armas_wbytes(wb)) < wsmin) {
        conf->error = ARMAS_EWORK;
        return -1;
    }
    // adjust blocking factor for workspace
    if (lb > 0 && A->cols > lb) {
        wsz /= sizeof(DTYPE);
        if (wsz < A->cols * lb) {
            lb = (wsz / A->cols) & ~0x3;
            if (lb < ARMAS_BLOCKING_MIN)
                lb = 0;
        }
    }

    wsz = armas_wpos(wb);
    buf = (DTYPE *) armas_wptr(wb);

    if (lb == 0 || A->cols <= lb) {
        armas_x_make(&Wrk, A->cols, 1, A->cols, buf);
        unblk_qrbuild(A, (armas_x_dense_t *) tau, &Wrk, A->rows-K,
                      A->cols-K, TRUE, conf);
    } else {
        // block reflector at start of workspace; other temporary space after
        armas_x_make(&T, lb, lb, lb, buf);
        armas_x_make(&Wrk, A->cols-lb, lb, A->cols-lb, &buf[armas_x_size(&T)]);

        blk_qrbuild(A, (armas_x_dense_t *) tau, &T, &Wrk, K, lb, conf);
    }
    armas_wsetpos(wb, wsz);

    return 0;
}
